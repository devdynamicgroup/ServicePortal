function t(key, fallback) {
  const lang = S?.lang || 'en';
  return I18N[lang]?.[key] ?? I18N.en?.[key] ?? fallback ?? key;
}

function applyI18n(lang) {
  const L = lang || S.lang || 'en';
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    const val = I18N[L]?.[key] ?? I18N.en?.[key];
    if (val != null) el.textContent = val;
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.dataset.i18nPlaceholder;
    const val = I18N[L]?.[key] ?? I18N.en?.[key];
    if (val != null) el.placeholder = val;
  });
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.dataset.i18nHtml;
    const val = I18N[L]?.[key] ?? I18N.en?.[key];
    if (val != null) el.innerHTML = val;
  });
}

function refreshDynamicI18n() {
  if (typeof updateProvinceOptions === 'function') updateProvinceOptions();
  if (typeof updatePreassessmentOptionText === 'function') updatePreassessmentOptionText();
  if (typeof renderJobSteps === 'function' && S.activeJob) renderJobSteps();
  if (typeof renderJobs === 'function') renderJobs();
  if (typeof renderTapTabs === 'function') renderTapTabs();
  if (typeof renderAssessList === 'function') renderAssessList();
  if (typeof MeterReadingCapture !== 'undefined' && MeterReadingCapture.renderSession) {
    MeterReadingCapture.renderSession();
  }
  if (S.screen === 's-score' && typeof calcAndShowScore === 'function') calcAndShowScore();
  if (typeof updatePaymentScreen === 'function' && document.getElementById('s-payment')) updatePaymentScreen();
  if (typeof updatePreassessBtn === 'function') updatePreassessBtn();
  if (typeof updateProvinceOptions === 'function') updateProvinceOptions();
}

function setLanguage(lang, options = {}) {
  S.lang = lang;
  localStorage.setItem('wm-lang', lang);
  document.getElementById('lang-en')?.classList.toggle('sel', lang === 'en');
  document.getElementById('lang-th')?.classList.toggle('sel', lang === 'th');
  document.querySelectorAll('.login-lang .lang-btn').forEach((b, i) => {
    b.classList.toggle('sel', (lang === 'en' && i === 0) || (lang === 'th' && i === 1));
  });
  applyI18n(lang);
  refreshDynamicI18n();
  if (!options.silent) {
    if (typeof closeLangModal === 'function') closeLangModal();
    showToast(lang === 'th' ? t('toast.langTh') : t('toast.langEn'));
  }
}
